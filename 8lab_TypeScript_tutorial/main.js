"use strict";
// ---LET---
// - In TypeScript, let is a keyword used to declare a block-scoped local 
// variable, optionally initializing it to a value.
// - One of the key characteristics of let is that it is block-scoped, meaning 
// a variable declared with let is only accessible within the nearest enclosing 
// block (denoted by {}).
let x = 10;
console.log(x);
let message = "Hello, TypeScript!";
let heading = document.createElement("h1");
heading.textContent = message;
document.body.appendChild(heading);
